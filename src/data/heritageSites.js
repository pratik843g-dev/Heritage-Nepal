export const getAllSites = () => Object.values(heritageSites);

export const getSiteById = (id) => heritageSites[id] || null;

export const getAllSiteIds = () => Object.keys(heritageSites);

export const searchSites = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return getAllSites().filter(site => 
    site.name.toLowerCase().includes(lowercaseQuery) ||
    site.location.toLowerCase().includes(lowercaseQuery) ||
    site.type.toLowerCase().includes(lowercaseQuery)
  );
};
